import axios from 'axios';
import { getToken, removeToken, removeUser } from '../utils/auth.jsx';

const API_BASE_URL = import.meta.env.VITE_API_BASE_URL || 'http://localhost:5000/api';

const api = axios.create({
  baseURL: API_BASE_URL,
  headers: {
    'Content-Type': 'application/json',
  },
  withCredentials: true, // âœ… ADD THIS FOR CORS
});

// Add token to requests
api.interceptors.request.use(
  (config) => {
    const token = getToken();
    if (token) {
      config.headers.Authorization = `Bearer ${token}`;
    }
    return config;
  },
  (error) => {
    return Promise.reject(error);
  }
);

// Handle token expiration
api.interceptors.response.use(
  (response) => response,
  (error) => {
    if (error.response?.status === 401) {
      removeToken();
      removeUser();
      window.location.href = '/login';
    }
    return Promise.reject(error);
  }
);

// Authentication API
export const authAPI = {
  register: (userData) => api.post('/auth/register', userData),
  login: (credentials) => api.post('/auth/login', credentials),
  getProfile: () => api.get('/auth/profile'),
};

// Chat API
export const chatAPI = {
  sendMessage: (messageData) => api.post('/chat/message', messageData),
  getChats: () => api.get('/chat/chats'),
  getChat: (chatId) => api.get(`/chat/chat/${chatId}`),
};

// Health check
export const healthCheck = () => api.get('/');

export default api;